%ERA5每一场台风周围垂直风切变的计算
%update202205010
clear; clc; close all;
header=['ncols           720';
        'nrows           360';
        'xllcorner      -180';
        'yllcorner       -90';
        'cellsize        0.5';
        'NODATA_value  -9999'];
    
TC_idir='E:\DATA\IBTrACS\NEW\IBTrACS.ALL.v04r00.nc';
files_idir ='F:\GlobalTCMask1\TCraster500km\';
raster_files = dir([files_idir,'*.txt']);
ERA5_idir_200U='E:\DATA\ERA5\ERA5_UCW_200\';
ERA5_idir_200V='E:\DATA\ERA5\ERA5_VCW_200\';
ERA5_idir_850U='E:\DATA\ERA5\ERA5_UCW_850\';
ERA5_idir_850V='E:\DATA\ERA5\ERA5_VCW_850\';
ERAfn_200U='ERA5_U_Component_Of_Wind_on_200hpa_levels_daily';
ERAfn_200V='ERA5_V_Component_Of_Wind_on_200hpa_levels_daily';
ERAfn_850U='ERA5_U_Component_Of_Wind_on_850hpa_levels_daily';
ERAfn_850V='ERA5_V_Component_Of_Wind_on_850hpa_levels_daily';
odir='\';


[lmheader1,lmheader2,Clandmask] = read_ARCascii('C:\Users\Dell\Desktop\Desktop2\Global_cyclone_project\Picture_materials\ARCGIS\Rastermasks\countriesmasks.txt');%陆地和国家
Clandmask=[Clandmask(:,361:720),Clandmask(:,1:360)];%-180-180 根据输入数据的extend进行修改
[Garea025,~] = Gridarea(0.25);
[Garea05,~] = Gridarea(0.5);
[SID,LAT,LON,ISO_TIME,~,~,~,~,~,~,~,~]=IBTrACS_nc_entire_variable_r(TC_idir);

S_TC_Zv=zeros(8196,1);%每场台风的降水
parfor NO=8899:13476 %注意年分1950开始
    
time=ISO_TIME(:,:,NO);
lat=LAT(:,NO);       %从上到下是不是对应台风的时间从前到后还是反过来的？？？
lat(isnan(lat) )=[];
lon=LON(:,NO);
lon(isnan(lon) )=[]; 

%一场台风每点位(每三小时)的日尺度时间点数值形式get
year=cell(length(lat));%预分配内存
month=cell(length(lat));
day=cell(length(lat));
numtime=zeros(length(lat),1);

  for i=1:length(lat)
    year(i)=cellstr((time(1:4,i))');%将字符数组转换为cell型字符串数组:字符数组S中的每行分割成为cell细胞元组C的一个元素,并删除S的每行尾部空格
    month(i)=cellstr((time(6:7,i))');
    day(i)=cellstr((time(9:10,i))');
    numtime(i,1)=str2double([time(1:4,i)',time(6:7,i)',time(9:10,i)']);%以数字形式储存时间方便去重
  end
  
            
%读acrtxt头文件和掩膜 %header   %1--ncols  %2--nrows  %3--xllcorner  %4--yllcorner  %5--cellsize  %6--NODATA_value
[~,inputheader,ras]= read_ARCascii ([files_idir,raster_files(NO-5305).name]); %注意文件名虽然不连续但是时间顺序是连续的就不会影响

%process 1 globalize the imcomplete ras(ter) 任意格网大小arctxt处理成全球的
raster = globalize_the_imcomplete_raster(inputheader,ras);
TCZv =ones(180/inputheader(5),360/inputheader(5))*-9999; %每一次都要重新赋值,要想加求年降水的话不能-999，全零就可以了

%process 2 calculate time of each grid from TCRoutedata
[weizhi,passyear,passmonth,passday,locationR,locationC] = raster_grid_TC_pass_time(inputheader,raster,year,month,day,lon,lat);
%注意这里输出的是横向0-360而下面直接进数据集里面读取的是竖向-180-180

%process 3 preread all txt--saving time
[DB,MB,YB,~,~,~] = daybackdayforward(str2double(char(day(1))),str2double(char(month(1))),str2double(char(year(1))));%第一天之前的两天
[~,~,~,DF,MF,YF] = daybackdayforward(str2double(char(day(length(numtime)))),str2double(char(month(length(numtime)))),str2double(char(year(length(numtime)))));%第一天之后的两天
allday=[str2double([YB,MB,DB]);numtime;str2double([YF,MF,DF])];%需要补充2021年1月1日的数据
allday=unique(allday);%unique默认去重之后升序排序
allW_200U=zeros(360,720,length(allday));%初始化
allW_200V=zeros(360,720,length(allday));%初始化
allW_850U=zeros(360,720,length(allday));%初始化
allW_850V=zeros(360,720,length(allday));%初始化
All_Zv       =zeros(360,720,length(allday));%初始化
for k=1:length(allday)
    STRdate = num2str(allday(k));
    
    Amonthdata_200U = ncread([ERA5_idir_200U,ERAfn_200U,STRdate(1:6),'.nc'],'u');%每一个allday对应一个allpre
    Amonthdata_200V = ncread([ERA5_idir_200V,ERAfn_200V,STRdate(1:6),'.nc'],'v');%每一个allday对应一个allpre
    Amonthdata_850U = ncread([ERA5_idir_850U,ERAfn_850U,STRdate(1:6),'.nc'],'u');%每一个allday对应一个allpre
    Amonthdata_850V = ncread([ERA5_idir_850V,ERAfn_850V,STRdate(1:6),'.nc'],'v');%每一个allday对应一个allpre
    
    allW_200U(:,:,k)=ERA5dailyresample( flipud(Amonthdata_200U(:,:,str2double(STRdate(7:8)))') ,Garea025); %读取出来的ERA5原数据经过重采样到标准网格 unit:m/s
    allW_200V(:,:,k)=ERA5dailyresample( flipud(Amonthdata_200V(:,:,str2double(STRdate(7:8)))') ,Garea025); %读取出来的ERA5原数据经过重采样到标准网格 unit:m/s
    allW_850U(:,:,k)=ERA5dailyresample( flipud(Amonthdata_850U(:,:,str2double(STRdate(7:8)))') ,Garea025); %读取出来的ERA5原数据经过重采样到标准网格 unit:m/s
    allW_850V(:,:,k)=ERA5dailyresample( flipud(Amonthdata_850V(:,:,str2double(STRdate(7:8)))') ,Garea025); %读取出来的ERA5原数据经过重采样到标准网格 unit:m/s
    
    Diff_UW=allW_200U(:,:,k)-allW_850U(:,:,k);
    Diff_VW=allW_200V(:,:,k)-allW_850V(:,:,k);
    All_Zv(:,:,k)=sqrt( (Diff_UW.^2) + (Diff_VW.^2) );%extra_process calculating |Zv| >0
end

%process 4 calculate Precipitation of each grid 
  for j=1:(length(weizhi))
          
      if locationC(j)>180/inputheader(5)    %0》360to-180》180回归到数据原始文档里排序
        locationC(j)=locationC(j)-180/inputheader(5);
      else
        locationC(j)=locationC(j)+180/inputheader(5);
      end

      %后面已经行列互换了，所以这里不再行列交换(转置)
       
      [dayB,monthB,yearB,dayF,monthF,yearF] = daybackdayforward(str2double(char(passday(j))),str2double(char(passmonth(j))),str2double(char(passyear(j))));
   
     NOdataexclute=zeros(3,1);
      
      Zv1 =All_Zv(:,:, allday==str2double([yearB,monthB,dayB]) );
      NOdataexclute(1,1)=Zv1(locationR(j),locationC(j));
      
      Zv2 =All_Zv(:,:, allday==str2double([char(passyear(j)),char(passmonth(j)),char(passday(j))]) );
      NOdataexclute(2,1)=Zv2(locationR(j),locationC(j));
      
      Zv3 =All_Zv(:,:, allday==str2double([yearF,monthF,dayF]) );
      NOdataexclute(3,1)=Zv3(locationR(j),locationC(j));
      
        
      NOdataexclute(NOdataexclute<0)=[];%降水坏点-9999去除
      NOdataexclute(isnan(NOdataexclute))=[];
      TCZv(locationR(j),locationC(j)) = mean(NOdataexclute);%-180180，位置不变，注意
  end
  
%OUTPUT(odir,['ERA5_Ori_SingleTC_pre','_SID',SID(:,NO)'],header,TCpre);
Zvcurrent=TCZv;
Zvcurrent(Zvcurrent<0)=0;%nonvalue变成0可以求和
Zvcurrent(isnan(Zvcurrent))=0;%nonvalue变成0可以求和
S_TC_Zv(NO-5305)=sum(sum(Zvcurrent.*Garea05))/sum(sum(Garea05(Zvcurrent>0)));%每场台风的降水量 十亿m3
end
